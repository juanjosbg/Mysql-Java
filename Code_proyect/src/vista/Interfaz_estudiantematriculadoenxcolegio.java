    public void consulta() {
        Matricula objp = new Matricula();
        String sql = "SELECT * FROM estudiantematriculadoenxcolegio";
        LinkedList<estudiantematriculadoenxcolegio> lpc = objp.buscaraestudiantematriculadoenxcolegio(sql);

        for (int i = 0; i < lpc.size(); i++) {
            estudiantematriculadoenxcolegio get = lpc.get(i);
            jTextArea1.append("Nombre Estudiante: " + get.getNombreEstudiante() + " " + get.getNombreEstudiante_2() + " " + get.getApellidoEstudiante() + " " + get.getApellidoEstudiante_2() + "\n");
            jTextArea1.append("Fecha Matricula : " + get.getFechaMatricula() + "\n");
            jTextArea1.append("Grado: " + get.getNombreG() + "\n");
            jTextArea1.append("Colegio: " + get.getNombreColegio() + "\n");
            jTextArea1.append("\n");
        }
    }

