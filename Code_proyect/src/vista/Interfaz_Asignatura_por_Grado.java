    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String FechaInicio = jTextField1.getText();
        String FechaFin = jTextField2.getText();
        String grado = String.valueOf(jComboBox1.getSelectedItem());
        String docente = String.valueOf(jComboBox2.getSelectedItem());
        String asignatura = String.valueOf(jComboBox3.getSelectedItem());
        int idgrado = 0;

        for (int i = 0; i < listaG.size(); i++) {    // Recorre las lista grado y el if es una validacion para poder tomar la variable del nombre //
            Grado get = listaG.get(i);
            if (grado.equals(get.getNombreG())) {
                idgrado = get.getIdGrado();
            }
        }
        int iddocente = 0;
        for (int i = 0; i < listaD.size(); i++) {
            Docente get = listaD.get(i);
            if (docente.equals(get.getNombreDocente())) { //LO MISMO 
                iddocente = get.getIdDocente();
            }
        }
        int idasignatura = 0;
        for (int i = 0; i < listaA.size(); i++) {               // LO MISMO 
            Asignatura get = listaA.get(i);
            if (asignatura.equals(get.getNombreAsignatura())) {
                idasignatura = get.getIdAsignatura();
            }
        }

        Asignatura_por_grado objc = new Asignatura_por_grado(FechaInicio, FechaFin, idgrado, iddocente, idasignatura); //Obj de apg con las variables que se van a pedir //
        Controller_Asignatura_por_Grado objcc = new Controller_Asignatura_por_Grado(); // obj controller que regula lo creado en el obj anterior //

        boolean E = objcc.insertarAsignatura_por_Grado(objc); // Luego viene la inserccion en el metodo insertarAPG

        if (E) {
            JOptionPane.showMessageDialog(null, "Se agregaron con exito");
        } else {
            JOptionPane.showMessageDialog(null, "Error al insertar dato");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened

        //LLAMAMOS LOS NOMBRES DE LOS GRADOS
        Controller_Grado objcc = new Controller_Grado();

        listaG = objcc.insertandoGrado();

        for (int i = 0; i < listaG.size(); i++) {
            Grado Grado = listaG.get(i);
            jComboBox1.addItem(Grado.getNombreG());

        }
        //LLAMAMOS LOS NOMBRES DE LOS DOCENTES
        Controller_docente objdd = new Controller_docente();

        listaD = objdd.consultarDocente();

        for (int i = 0; i < listaD.size(); i++) {
            Docente Docente = listaD.get(i);
            jComboBox2.addItem(Docente.getNombreDocente() + " " + Docente.getNombreDocente2() + " " + Docente.getApellidoDocente() + " " + Docente.getApellidoDocente2() + " ");

        }

        //LLAMAMOS LOS NOMBRES DE LAS ASIGNATURAS
        Controller_Asignatura objaa = new Controller_Asignatura();
        listaA = objaa.insertarAsignatura();

        for (int i = 0; i < listaA.size(); i++) {
            Asignatura Asignatura = listaA.get(i);
            jComboBox3.addItem(Asignatura.getNombreAsignatura());

        }


    }//GEN-LAST:event_formWindowOpened

