        // TODO add your handling code here:
        Controller_estudiante objEstudiante = new Controller_estudiante();
        idEstudianteenSesion = Integer.parseInt(jTextField5.getText());
//        String idE = jTextField5.getText();
        listae = objEstudiante.consultarEstudiante(idEstudianteenSesion);

        String Correo = jTextField3.getText();

        for (int i = 0; i < listae.size(); i++) {
            Estudiante e = listae.get(i);
            if (Correo.equals(e.getCorreo()) && idEstudianteenSesion == e.getIdEstudiante()) {
                texto1 = e.getNombreEstudiante() + " " + e.getNombreEstudiante_2() + " " + e.getStringApellidoEstudiante() + " " + e.getApellidoEstudiante_2();
                Interfaz_estudiante__start ventana = new Interfaz_estudiante__start();
                ventana.setVisible(true);
                ventana.pack();
//                Ins.setLocationRelativeTo(null);
//                Ins.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                dispose();

            } else {
                JOptionPane.showConfirmDialog(this, "Verifique que todos los datos esten digitados o esten correctos");
            }
        }
