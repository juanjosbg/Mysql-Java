    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        Controller_estudiante objee = new Controller_estudiante();
        int ide = Integer.parseInt(jTextField9.getText());
        listaE = objee.consultarEstudiante(ide);

        for (int i = 0; i < listaE.size(); i++) {
            Estudiante e = listaE.get(i);
            jTextField1.setText(e.getNombreEstudiante());
            jTextField4.setText(e.getNombreEstudiante_2());

            jTextField2.setText(e.getStringApellidoEstudiante());
            jTextField7.setText(e.getApellidoEstudiante_2());

            jTextField3.setText(e.getTelefono());

            jTextField5.setText(e.getCorreo());

            jTextField6.setText(String.valueOf(e.getIdgenero()));

            jTextField8.setText(e.getFechaNacimiento());
        }


    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int idestudiant = Integer.parseInt(jTextField9.getText());
        String nomE1 = jTextField1.getText();
        String nomE2 = jTextField4.getText();
        String apeE1 = jTextField2.getText();
        String apeE2 = jTextField7.getText();

        String teleE = jTextField3.getText();
        String correoE = jTextField5.getText();
        String fechaE = jTextField8.getText();
        int idgeneroE = Integer.parseInt(jTextField6.getText());

        Estudiante objEstudiante = new Estudiante(idestudiant, nomE1, nomE2, apeE1, apeE2, teleE, correoE, fechaE, idgeneroE);
        Controller_estudiante objee = new Controller_estudiante();
        boolean t = objee.updatep(objEstudiante);
        if (t = true) {
            JOptionPane.showMessageDialog(null, "Se ha modificado el estudiante con √©xito");
        } else {
            JOptionPane.showMessageDialog(null, "No se ha modificado el estudiante");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

