    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        Double nota1 = Double.parseDouble(jTextField1.getText());
        Double nota2 = Double.parseDouble(jTextField5.getText());
        Double nota3 = Double.parseDouble(jTextField3.getText());
        Double notaF = Double.parseDouble(jTextField4.getText());
        String estu = String.valueOf(jComboBox1.getSelectedItem());
        String[] parts = estu.split(" ");
        String part1 = parts[0];//Nombre1
        String part2 = parts[1];//Nombre2
        String part3 = parts[2];//Apellido1
        String part4 = parts[3];//Apellido2

        int idAPG = Integer.parseInt(jTextField2.getText());

        int idestu = 0;
        for (int i = 0; i < listaE.size(); i++) {
            Estudiante get = listaE.get(i);
            if (part1.equals(get.getNombreEstudiante()) && part3.equals(get.getStringApellidoEstudiante())) {
                idestu = get.getIdEstudiante();
            }
        }
//        System.out.println("id estudiante: " + idestu);
//        System.out.println("id asignatura por pagina: " + idAPG);
        Nota objc = new Nota(nota1, nota2, nota3, notaF, idestu, idAPG);
        Controller_Nota objcc = new Controller_Nota();

        boolean E = objcc.insertNota(objc);

        if (E) {
            JOptionPane.showMessageDialog(null, "Se agregaron con exito");
        } else {
            JOptionPane.showMessageDialog(null, "Error al insertar dato");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        //LLAMAMOS LOS NOMBRES DE LOS ESTUDIANTES
        Controller_estudiante objee = new Controller_estudiante();
        consultarasignaturasdexgrados objAPG = new consultarasignaturasdexgrados();

        int idR = Integer.parseInt(jLabel9.getText());

        listaE = objee.consultarEstudianteDeDichoDocente(idR);
        listaAPG = objAPG.consultarApgName(idR);
        for (int i = 0; i < listaAPG.size(); i++) {
            consultarasignaturasdexgrados s = listaAPG.get(i);
            jTextField6.setText(s.getNombreAsignatura()); //Asignatura name
            jTextField2.setText(String.valueOf(s.getIdAPG())); //Id APG
        }

        for (int i = 0; i < listaE.size(); i++) {
            Estudiante e = listaE.get(i);
            jComboBox1.addItem(e.getNombreEstudiante() + " " + e.getNombreEstudiante_2() + " " + e.getStringApellidoEstudiante() + " " + e.getApellidoEstudiante_2() + " ");

        }
    }//GEN-LAST:event_formWindowOpened

